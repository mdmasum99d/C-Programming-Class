#include<stdio.h>
int main()
{
    int a,b,c;
    printf("Enter the value of a : ");
    scanf("%d",&a);
    printf("Enter the value of b : ");
    scanf("%d",&b);
    printf("Enter the value of c : ");
    scanf("%d",&c);
    printf("%d + %d + %d = %d\n",a,b,c,a+b+c);
    printf("%d * %d * %d = %d\n",a,b,c,a*b*c);
}
